CREATE SCHEMA IF NOT EXISTS courseschema;
USE courseschema;

CREATE TABLE IF NOT EXISTS USERS (
  USER_ID INT NOT NULL AUTO_INCREMENT,
  SURNAME VARCHAR(32) NOT NULL,
  FIRSTNAME VARCHAR(32) NOT NULL,
  PHONE INT NOT NULL,
  PRIMARY KEY (USER_ID)
);

CREATE TABLE IF NOT EXISTS COMPUTERS (
  COMPUTER_ID INT NOT NULL AUTO_INCREMENT,
  USER_ID INT NOT NULL,
  VENDOR VARCHAR(32) NOT NULL,
  MODEL VARCHAR(32) NOT NULL,
  DEFFECT VARCHAR(32) NOT NULL,
  PRICE VARCHAR(32) NOT NULL,
  REPAIRED VARCHAR(32) NOT NULL,
  PRIMARY KEY (COMPUTER_ID, USER_ID),
  INDEX USERID_idx (USER_ID ASC) VISIBLE,
  CONSTRAINT USERID FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID)
);